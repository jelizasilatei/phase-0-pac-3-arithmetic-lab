//basic math fucntions
function add(x,y){
    return x+y;
}

function subtract(x,y){
    return x-y;
}

function multiply(x,y){
    return x*y;
}

function divide(x,y){
    return x/y;
}

function increment(n){
    return(n += 1);
}

function decrement(n){
    return(n -= 1);
}


function makeInt(num){
    return parseInt(num, 10);
}

function preserveDecimal(num){
    return parseFloat(num);
}